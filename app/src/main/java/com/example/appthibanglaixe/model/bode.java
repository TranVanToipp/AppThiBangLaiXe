package com.example.appthibanglaixe.model;

public class bode {
    private int socau;
    private String sobode;

    public bode(int socau, String sobode) {
        this.socau = socau;
        this.sobode = sobode;
    }

    public int getSocau() {
        return socau;
    }

    public void setSocau(int socau) {
        this.socau = socau;
    }

    public String getSobode() {
        return sobode;
    }

    public void setSobode(String sobode) {
        this.sobode = sobode;
    }
}
